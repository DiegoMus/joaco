      <p>
      Egon Spengler es un miembro del equipo, alto, lacónico, con gafas y torpe, responsable del principal marco teórico de sus estudios paranormales/cuánticos. Adicto a la ciencia, es el creador del equipo de los Cazafantasmas junto con Raymond Stantz, lo que le convierte en el cerebro de los Cazafantasmas. Aunque es inteligente para los libros, Spengler no tiene mucha capacidad social, como demuestran sus rígidas interacciones con la secretaria de los Cazafantasmas, Janine Melnitz, y su dependencia de Peter Venkman como portavoz del grupo.
      </p>
      <p>
      Spengler es el miembro más serio y rígido del equipo. De sus aficiones, Spengler declara que colecciona "esporas, mohos y hongos", y afirma que, de niño, el único juguete que tuvo fue "parte de un Slinky", que enderezó. Como se insinúa en la primera película, Spengler es aparentemente un adicto al azúcar, debido a su afición por los dulces y las golosinas. Según el videojuego de 2009, Spengler duerme una media de 14 minutos al día, lo que le deja "mucho tiempo para trabajar".
